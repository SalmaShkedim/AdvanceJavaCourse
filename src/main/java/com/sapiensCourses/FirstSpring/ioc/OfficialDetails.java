package com.sapiensCourses.FirstSpring.ioc;

public class OfficialDetails {
    String edu_qualification;
    String percentage;
    String Designation;
    String years_of_experience;
    String current_comp_name;

    public String getEdu_qualification() {
        return edu_qualification;
    }

    public void setEdu_qualification(String edu_qualification) {
        this.edu_qualification = edu_qualification;
    }

    public String getPercentage() {
        return percentage;
    }

    public void setPercentage(String percentage) {
        this.percentage = percentage;
    }

    public String getDesignation() {
        return Designation;
    }

    public void setDesignation(String designation) {
        Designation = designation;
    }

    public String getYears_of_experience() {
        return years_of_experience;
    }

    public void setYears_of_experience(String years_of_experience) {
        this.years_of_experience = years_of_experience;
    }

    public String getCurrent_comp_name() {
        return current_comp_name;
    }

    public void setCurrent_comp_name(String current_comp_name) {
        this.current_comp_name = current_comp_name;
    }
}
